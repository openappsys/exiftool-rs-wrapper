fn create_exiftool() -> Result<ExifTool, Error> {
    if let Ok(path) = std::env::var("EXIFTOOL_PATH") {
        return ExifTool::builder().executable(path).build();
    }

    ExifTool::new()
}

#[test]
fn execute_text_should_not_force_json() {
    let exiftool = match create_exiftool() {
        Ok(et) => et,
        Err(Error::ExifToolNotFound) => return,
        Err(e) => panic!("Unexpected error: {:?}", e),
    };

    let temp = tempfile::tempdir().expect("failed to create temp dir");
    let file = temp.path().join("one.jpg");
    fs::write(&file, TINY_JPEG).expect("failed to write image");

    let text = exiftool
        .query(&file)
        .tag("FileName")
        .execute_text()
        .expect("execute_text should succeed");

    assert!(!text.trim().is_empty());
    assert!(!text.trim_start().starts_with('['));
}

#[test]
fn list_writable_tags_should_match_raw_probe_semantics() {
    let exiftool = match create_exiftool() {
        Ok(et) => et,
        Err(Error::ExifToolNotFound) => return,
        Err(e) => panic!("Unexpected error: {:?}", e),
    };

    let from_api = exiftool
        .list_writable_tags()
        .expect("list_writable_tags should succeed");
    let from_raw = exiftool
        .execute(&["-listw"])
        .expect("raw -listw should succeed")
        .text()
        .split_whitespace()
        .count();

    assert_eq!(from_api.len(), from_raw);
}
