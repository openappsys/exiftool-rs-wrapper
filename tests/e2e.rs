#[test]
fn e2e_read_write_query_batch() {
    let exiftool = match ExifTool::new() {
        Ok(et) => et,
        Err(Error::ExifToolNotFound) => return,
        Err(e) => panic!("Unexpected error: {:?}", e),
    };

    let temp = tempfile::tempdir().expect("failed to create temp dir");
    let file1 = temp.path().join("a.jpg");
    let file2 = temp.path().join("b.jpg");

    fs::write(&file1, TINY_JPEG).expect("failed to write image a");
    fs::write(&file2, TINY_JPEG).expect("failed to write image b");

    // write
    exiftool
        .write(&file1)
        .tag("Artist", "E2E")
        .overwrite_original(true)
        .execute()
        .expect("write should succeed");

    // read
    let artist: String = exiftool
        .read_tag(&file1, "Artist")
        .expect("read tag should succeed");
    assert_eq!(artist, "E2E");

    // query_batch
    let results = exiftool
        .query_batch(&[&file1, &file2])
        .tag("FileName")
        .execute()
        .expect("batch query should succeed");
    assert_eq!(results.len(), 2);
}
